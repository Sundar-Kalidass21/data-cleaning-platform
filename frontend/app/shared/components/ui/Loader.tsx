type LoaderProps = {
  size?: "sm" | "md" | "lg"
}

export function Loader({ size = "md" }: LoaderProps) {
  const sizes = {
    sm: "h-4 w-4 border-2",
    md: "h-6 w-6 border-2",
    lg: "h-10 w-10 border-4",
  }

  return (
    <div
      className={`
        ${sizes[size]}
        animate-spin
        rounded-full
        border-black
        border-t-transparent
      `}
    />
  )
}
